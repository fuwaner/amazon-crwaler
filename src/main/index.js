import { app, BrowserWindow, ipcMain } from 'electron'
import { spiderListen, resetData, exportData } from "./crawler/amazon";
import { taskListen } from './crawler/task'

/**
 * Set `__static` path to static files in production
 * https://simulatedgreg.gitbooks.io/electron-vue/content/en/using-static-assets.html
 */
if (process.env.NODE_ENV !== 'development') {
  global.__static = require('path').join(__dirname, '/static').replace(/\\/g, '\\\\')
}

let mainWindow
const winURL = process.env.NODE_ENV === 'development'
  ? `http://localhost:9080`
  : `file://${__dirname}/index.html`

function createWindow() {
  /**
   * Initial window options
   */
  mainWindow = new BrowserWindow({
    height: 563,
    useContentSize: true,
    width: 1000
  })

  mainWindow.loadURL(winURL)
  let winId = BrowserWindow.getFocusedWindow().id
  // 把渲染进程传递过来的数据再次传递给渲染进程news
  // 等待窗口加载完
  mainWindow.webContents.on('did-finish-load', () => {
    // 监听任务相关请求
    taskListen(mainWindow, winId)
    // 监听渲染进程爬取请求
    spiderListen(mainWindow, winId);
    // 监听重置数据
    resetData(mainWindow, winId);


  })
  mainWindow.on('closed', () => {
    mainWindow = null
  })
}

app.on('ready', createWindow)

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  if (mainWindow === null) {
    createWindow()
  }
})

